package com.spring.ormVO;

public class EmployeeVO {
	private int employee_key;
	private String employee_name;
	private String employee_job;
	private String employee_id;
	private String employee_pw;
	
	
	public String getEmployee_job() {
		return employee_job;
	}
	public void setEmployee_job(String employee_job) {
		this.employee_job = employee_job;
	}
	public String getEmployee_id() {
		return employee_id;
	}
	public void setEmployee_id(String employee_id) {
		this.employee_id = employee_id;
	}
	public String getEmployee_pw() {
		return employee_pw;
	}
	public void setEmployee_pw(String employee_pw) {
		this.employee_pw = employee_pw;
	}
	public int getEmployee_key() {
		return employee_key;
	}
	public void setEmployee_key(int employee_key) {
		this.employee_key = employee_key;
	}
	public String getEmployee_name() {
		return employee_name;
	}
	public void setEmployee_name(String employee_name) {
		this.employee_name = employee_name;
	}
	

}
